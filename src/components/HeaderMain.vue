<template>
	<div class="header"></div>
</template>

<script>
import Konva from 'konva';

// const width = window.innerWidth;
// const height = window.innerHeight;

export default {
	name: 'HeaderMain',
	created() {
		// first we need to create a stage
		let stage = new Konva.Stage({
			container:  'container',
			width:      500,
			height:     500
		});

		// then create layer
		var layer = new Konva.Layer()

		// create our shape
		var circle = new Konva.Circle({
			x: stage.width() / 2,
			y: stage.height() / 2,
			radius: 70,
			fill: 'red',
			stroke: 'black',
			strokeWidth: 4
		});

// add the shape to the layer
		layer.add(circle);

// add the layer to the stage
		stage.add(layer);

// draw the image
		layer.draw();
	},
	data() {
	},
	methods: {},
	mounted() {
	},
	unmounted() {
	}
}
</script>